    private int getNewComboBoxIndex(int oldindex) //OK
    {
        int newindex= 0;
        for(int i = 0; i < myParent.getCountriesT().getIndexConnOfId().length; i++)
{
            if(myParent.getCountriesT().getIndexConnOfId()[i]==oldindex)
{
                newindex = i;
                break;
            }
        }
        return newindex;
    }
